#include "minirt.h"

int	ft_error(char *str)
{
	write(2, "Error\n", ft_strlen("Error\n"));
	if (str)
	{
		write(2, str, ft_strlen(str));
		write(2, "\n", 1);
	}
	exit (-1);
}
