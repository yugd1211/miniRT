#include "minirt.h"

int	ft_error(char *str)
{
	write(1, "ERROR\n", ft_strlen("ERROR\n"));
	if (str)
	{
		write(1, str, ft_strlen(str));
		write(1, "\n", 1);
	}
	exit (-1);
}
